    private void EditbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditbtnActionPerformed
         int fila = FrmTable.getSelectedRow();
    if (fila < 0) {
        JOptionPane.showMessageDialog(this, "Seleccione una fila para editar.");
        return;
    }

    int id = (int) tblModel.getValueAt(fila, 0);
    String nuevoNombre = jTextField1.getText().trim();
    String nuevoNivel = jComboBox1.getSelectedItem().toString();

    if (nuevoNombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe escribir un idioma.");
        return;
    }

    Idioma idioma = idiomaService.findById(id);
    idioma.setNombreIdioma(nuevoNombre);
    //idioma.setNivel(nuevoNivel);

    if (idiomaService.update(idioma)) {
        cargarIdiomasEnTabla();
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "Error al actualizar.");
    }
    }//GEN-LAST:event_EditbtnActionPerformed

