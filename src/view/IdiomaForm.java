private void cargarIdiomasEnTabla() {
    tblModel.setRowCount(0);
    List<Idioma> lista = idiomaService.getAll();
    for (Idioma idi : lista) {
        String nivel = "";  // si recién cargaste, nivel vacío
        tblModel.addRow(new Object[]{ idi.getIdIdioma(), idi.getNombreIdioma(), nivel });
    }
}
