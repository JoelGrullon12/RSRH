    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
    int fila = FrmTable.getSelectedRow();
    if (fila < 0) {
        JOptionPane.showMessageDialog(this, "Seleccione una fila para eliminar.");
        return;
    }

    int id = (int) tblModel.getValueAt(fila, 0);
    Idioma idioma = idiomaService.findById(id);

    int conf = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar este idioma?", "Confirmar", JOptionPane.YES_NO_OPTION);
    if (conf == JOptionPane.YES_OPTION) {
        if (idiomaService.delete(idioma)) {
            cargarIdiomasEnTabla();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar.");
        }
    }
    }//GEN-LAST:event_deletebtnActionPerformed

    
