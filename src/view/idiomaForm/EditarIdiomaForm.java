   private void editarIdioma() {
        if (idioma == null) {
            JOptionPane.showMessageDialog(this, "No hay idioma para editar.");
            return;
        }

        if (idiomaService == null) {
            JOptionPane.showMessageDialog(this, "Error: Servicio no disponible.");
            return;
        }

        String nuevoNombre = jTextField1.getText().trim();

        if (nuevoNombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre no puede estar vacÃ­o.");
            return;
        }

        try {
            idioma.setNombreIdioma(nuevoNombre);
            boolean actualizado = idiomaService.update(idioma);

            if (actualizado) {
                JOptionPane.showMessageDialog(this, "Idioma actualizado correctamente.");

                if (parentForm != null) {
                    parentForm.cargarIdiomasEnTabla();
                }

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar el idioma.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage());
            logger.log(java.util.logging.Level.SEVERE, "Error al actualizar idioma", e);
        }
    }
