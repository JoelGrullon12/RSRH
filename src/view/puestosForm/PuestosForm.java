    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Puesto nuevoPuesto=new Puesto();
        nuevoPuesto.setNombrePuesto(txtNombre.getText());
        nuevoPuesto.setRiesgoId(((Comboitem)cmbRiesgo.getSelectedItem()).getId());
        nuevoPuesto.setSalarioMinimo( new BigDecimal(txtSalarioMinimo.getValue().toString()));
        nuevoPuesto.setSalarioMaximo(new BigDecimal(txtSalarioMaximo.getValue().toString()));
        nuevoPuesto.setDepartamentoId(((Comboitem)cmbDepartamento.getSelectedItem()).getId());
        nuevoPuesto.setDescripcion(txtDesc.getText());

        if(!puestoService.insert(nuevoPuesto)){
            JOptionPane.showMessageDialog(this, "Error al insertar un nuevo puesto");
            return;
        }

        JOptionPane.showMessageDialog(this, "Puesto insertado correctamente");
        limpiarCampos();
        cargarPuestos();
        SwingUtilities.invokeLater(() -> {
            getParent().revalidate();
            getParent().repaint();
        });
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int filaSeleccionada = tblPuestos.getSelectedRow();
        if (filaSeleccionada < 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un idioma para editar.");
            return;
        }

        Puesto puestoSeleccionado = listaPuestos.get(filaSeleccionada);

        EditarPuestoForm editarIdiomaForm = new EditarPuestoForm(puestoSeleccionado, this);
        mainDesktop.add(editarIdiomaForm);
        editarIdiomaForm.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       int option= JOptionPane.showConfirmDialog(this, "Estas seguro que desea eliminar este puesto?", "Eliminar puesto", JOptionPane.YES_NO_OPTION);

       if(option!=JOptionPane.YES_OPTION){
        return;
       }

       int filaSeleccionada = tblPuestos.getSelectedRow();
        if (filaSeleccionada < 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un idioma para eliminar.");
            return;
        }

        Puesto puestoSeleccionado = listaPuestos.get(filaSeleccionada);
        puestoService.delete(puestoSeleccionado.getIdPuesto());

            JOptionPane.showMessageDialog(this, "Puesto eliminado correctamente");
            cargarPuestos();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void limpiarCampos(){
        txtNombre.setText("");
        txtDesc.setText("");
        txtSalarioMinimo.setValue(0);
        txtSalarioMaximo.setValue(0);
    }


