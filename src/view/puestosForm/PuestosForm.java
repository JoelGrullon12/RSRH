    private void initTable() {
        String[] header = {"ID", "Nombre", "Salario Max.", "Salario Min.", "Riesgo", "Departamento"};
        tblPuestosModel = new DefaultTableModel(header, 0);
        tblPuestos.setModel(tblPuestosModel);
    }

    private void cargarRiesgos(){
        List<RiesgoPuesto> listaRiesgos=riesgoService.getAll();

        cmbRiesgo.removeAllItems();

        for (RiesgoPuesto riesgoPuesto : listaRiesgos) {
            cmbRiesgo.addItem(new Comboitem(riesgoPuesto.getIdRiesgo(), riesgoPuesto.getNombreRiesgo()));
        }
    }

    private void cargarDepartamentos(){
        List<Departamento> listaDepas=depaService.getAll();

        cmbDepartamento.removeAllItems();

        for (Departamento depa : listaDepas) {
            cmbDepartamento.addItem(new Comboitem(depa.getIdDepartamento(), depa.getNombreDepartamento()));
        }
    }

    private void setupTableListener() {
        // Configurar botones inicialmente deshabilitados
        btnEditar.setEnabled(false);
        btnEliminar.setEnabled(false);

        // Agregar listener una sola vez para habilitar/deshabilitar botones al seleccionar fila
        tblPuestos.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) { // Evitar múltiples eventos
                    boolean filaSeleccionada = tblPuestos.getSelectedRow() != -1;
                    btnEditar.setEnabled(filaSeleccionada);
                    btnEliminar.setEnabled(filaSeleccionada);
                }
            }
        });
    }

    public void cargarPuestos(){
        try {
            listaPuestos = puestoService.getAllWithRelationships();
            
            // Limpiar tabla
            tblPuestosModel.setRowCount(0);
            
            // Llenar tabla con datos
            for (Puesto puesto : listaPuestos) {
                tblPuestosModel.addRow(new Object[]{
                    puesto.getIdPuesto(),   
                    puesto.getNombrePuesto(),   
                    puesto.getSalarioMinimo(), 
                    puesto.getSalarioMaximo(), 
                    puesto.getRiesgo().getNombreRiesgo(),
                    puesto.getDepartamento().getNombreDepartamento()
                    //idi.getNivel() != null ? idi.getNivel() : ""
                });
            }
            
            // Limpiar selección
            tblPuestos.clearSelection();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar puestos: " + e.getMessage());
            e.printStackTrace();
        }
    }

